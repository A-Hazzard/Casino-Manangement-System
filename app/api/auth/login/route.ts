/**
 * Login API Route
 *
 * This route handles user authentication and login functionality.
 * It supports:
 * - Email or username-based login
 * - Password validation
 * - Remember me functionality
 * - JWT token generation
 * - Session management
 * - Profile validation requirements
 * - Password update requirements
 * - Secure cookie management
 *
 * @module app/api/auth/login/route
 */

import { authenticateUser } from '@/app/api/lib/helpers/auth/auth';
import { connectDB } from '@/app/api/lib/middleware/db';
import {
  createBadRequestResponse,
  createErrorResponse,
  createServerErrorResponse,
  createSuccessResponse,
} from '@/app/api/lib/utils/apiResponse';
import { getFriendlyErrorMessage } from '@/lib/utils/auth';
import { getAuthCookieOptions } from '@/lib/utils/cookieSecurity';
import { getClientIP } from '@/lib/utils/ipAddress';
import { NextRequest } from 'next/server';
import {
  logRouteCreate,
  logRouteError,
  extractUserFromRequest,
} from '@/app/api/lib/utils/routeLogger';

export const runtime = 'nodejs';

/**
 * POST /api/auth/login
 *
 * Authenticates a user and issues JWT access and refresh token cookies. Called
 * on every login form submission; extends token lifetime when rememberMe is set.
 *
 * Body fields:
 * @param identifier {string} Required. Email address or username used to look up the account.
 * @param password   {string} Required. Plain-text password verified against the stored hash.
 * @param rememberMe {boolean} Optional. When true, extends cookie maxAge from 7 days to 30 days.
 */
export async function POST(request: NextRequest) {
  const startTime = Date.now();
  const functionName = 'POST /api/auth/login';
  const user = extractUserFromRequest(request);

  try {
    // ============================================================================
    // STEP 1: Connect to database
    // ============================================================================
    await connectDB();

    // ============================================================================
    // STEP 2: Parse and validate request body
    // ============================================================================
    const { identifier, password, rememberMe } = await request.json();

    // ============================================================================
    // STEP 3: Validate identifier format (email or username)
    // ============================================================================
    if (!identifier || !password) {
      return createBadRequestResponse(
        'Email/username and password are required.'
      );
    }

    const looksLikeEmail = /\S+@\S+\.\S+/.test(identifier);
    const isUsername =
      typeof identifier === 'string' && identifier.trim().length >= 3;

    if (!(looksLikeEmail || isUsername)) {
      return createBadRequestResponse('Invalid identifier format.');
    }

    // ============================================================================
    // STEP 4: Extract client IP and user agent
    // ============================================================================
    const ipAddress = getClientIP(request) || 'unknown';
    const userAgent = request.headers.get('user-agent') || 'unknown';

    // ============================================================================
    // STEP 5: Authenticate user with credentials
    // ============================================================================
    const result = await authenticateUser(
      identifier,
      password,
      ipAddress,
      userAgent,
      rememberMe
    );

    if (!result.success) {
      logRouteError(
        functionName,
        'POST',
        '/api/auth/login',
        result.message || 'Authentication failed',
        user
      );
      return createErrorResponse(
        result.message || 'Authentication failed',
        401
      );
    }

    if (!result.token) {
      logRouteError(
        functionName,
        'POST',
        '/api/auth/login',
        'No token generated by authenticateUser',
        user
      );
      return createServerErrorResponse(
        'Authentication failed - no token generated'
      );
    }

    // ============================================================================
    // STEP 6: Generate JWT tokens and create response
    // ============================================================================
    const response = createSuccessResponse(
      {
        user: result.user,
        expiresAt: result.expiresAt,
        requiresPasswordUpdate: result.requiresPasswordUpdate ?? false,
        requiresProfileUpdate: result.requiresProfileUpdate ?? false,
        invalidProfileFields: result.invalidProfileFields,
        invalidProfileReasons: result.invalidProfileReasons,
      },
      'Login successful'
    );

    // ============================================================================
    // STEP 7: Set secure authentication cookies
    // ============================================================================
    const clearOptions = getAuthCookieOptions(request, {
      expires: new Date(0),
    });
    response.cookies.set('token', '', clearOptions);
    response.cookies.set('refreshToken', '', clearOptions);

    const tokenMaxAge = rememberMe ? 30 * 24 * 60 * 60 : 7 * 24 * 60 * 60;
    const cookieOptions = getAuthCookieOptions(request, {
      maxAge: tokenMaxAge,
    });

    response.cookies.set('token', result.token!, cookieOptions);

    if (result.refreshToken) {
      response.cookies.set(
        'refreshToken',
        result.refreshToken,
        getAuthCookieOptions(request, { maxAge: tokenMaxAge })
      );
    }

    const duration = Date.now() - startTime;
    logRouteCreate(functionName, 'POST', '/api/auth/login', 1, user, duration);

    return response;
  } catch (error) {
    const errorMessage =
      error instanceof Error ? error.message : 'Login failed';
    logRouteError(functionName, 'POST', '/api/auth/login', errorMessage, user);
    return createServerErrorResponse(getFriendlyErrorMessage(errorMessage));
  }
}
